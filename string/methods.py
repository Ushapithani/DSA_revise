# let us consider s as input and perform all these list of functions on it 
s = " Hello, World! "
lst = [1,2,3,4,5]
print("Length of the string:", len(s))
print("Uppercase:", s.upper())
print("Lowercase:", s.lower())
print("Capitalized:", s.capitalize())
print("Replace 'World' with 'Python':", s.replace("World", "Python"))
print("Split the string:", s.split(", "))
# max and min functions
print("Max of the list:", max(lst))
print("Min of the list:", min(lst))

# sort 
print("Sort list:", sorted(lst))

#sorted 
print("Sorted list in reverse:", sorted(lst, reverse=True))

# sum 
print("Sum of the list:", sum(lst))

# ord 
print("Ordinal value of 'A':", ord('A'))










