# all sting methods
my_string = "Hello, World!"

# length of the string
print("Length of the string:", len(my_string))

# convert to uppercase
print("Uppercase:", my_string.upper())

# convert to lowercase
print("Lowercase:", my_string.lower())

# capitalize the first letter
print("Capitalized:", my_string.capitalize())

# replace a substring
print("Replace 'World' with 'Python':", my_string.replace("World", "Python"))

# split the string into a list

print("Split the string:", my_string.split(", "))

# check if the string starts with a specific substring
print("Does the string start with 'Hello'? ", my_string.startswith("Hello"))

# check if the string ends with a specific substring
print("Does the string end with 'World'? ", my_string.endswith("World"))

# find the index of a substring
print("Index of 'World':", my_string.find("World"))

# check if the string is alphanumeric
print("Is the string alphanumeric? ", my_string.isalnum())

# check if the string is alphabetic
print("Is the string alphabetic? ", my_string.isalpha())

# check if the string is numeric
print("Is the string numeric? ", my_string.isnumeric())



